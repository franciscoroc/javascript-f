let n=10

n++ //Pós-incremento, 11
console.log(n++) //11, 12 na próxima operação

console.log(n) //12

console.log(--n) //11, Pré-incremento

let x = -n // "-" é um elemento de inversão

console.log(x) //11 negativo

console.log(n + "" + x) // + quando há uma string junta os valores ao invés de somar